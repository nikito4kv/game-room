// Канонический некриптографический строковый хэш (FNV-1a, 32 бита) — одна
// реализация на проект, без зависимостей. Не для безопасности: задача —
// детерминированная необратимая метка с низкими коллизиями.
//
// Примечание: hueForIdentity (src/lib/chat.ts) намеренно использует свою
// rolling-mod-360 математику — у неё другая область значений (оттенок 0..359) и
// зафиксированный тестами контракт (hueForIdentity("") === 0), поэтому она не
// сводится к этому помощнику без смены цветов у существующих участников.

/** FNV-1a 32-бит. Возвращает беззнаковое 32-битное число. */
export function fnv1a32(str: string): number {
  let h = 0x811c9dc5;
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i);
    // умножение на простое FNV через Math.imul (корректная 32-битная арифметика)
    h = Math.imul(h, 0x01000193);
  }
  return h >>> 0;
}

/**
 * Необратимый короткий хэш кода комнаты для тега корреляции в Sentry (не сам
 * код — приватность, см. дизайн-спеку §5). Берём два FNV с разным «солением» и
 * склеиваем base36 → ~64-битное пространство, чтобы коллизии были маловероятны
 * даже на многих тысячах комнат.
 */
export function hashRoomCode(code: string): string {
  const a = fnv1a32(code);
  const b = fnv1a32(`${code}room`);
  return a.toString(36) + b.toString(36);
}
